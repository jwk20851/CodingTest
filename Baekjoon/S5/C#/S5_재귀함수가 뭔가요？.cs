using System;

namespace Baekjoon
{
    class Program
    {
        static StreamWriter sw = new(new BufferedStream(Console.OpenStandardOutput()));
        static string[] arr = {
                "\"재귀함수가 뭔가요?\"",
                "\"잘 들어보게. 옛날옛날 한 산 꼭대기에 이세상 모든 지식을 통달한 선인이 있었어.",
                "마을 사람들은 모두 그 선인에게 수많은 질문을 했고, 모두 지혜롭게 대답해 주었지.",
                "그의 답은 대부분 옳았다고 하네. 그런데 어느 날, 그 선인에게 한 선비가 찾아와서 물었어.\"",
                "\"재귀함수는 자기 자신을 호출하는 함수라네\"",
                "라고 답변하였지.",
                "____"
            };

        static void Main(string[] args)
        {
            int n = int.Parse(Console.ReadLine());
            int m = n;
            sw.WriteLine("어느 한 컴퓨터공학과 학생이 유명한 교수님을 찾아가 물었다.");
            call(0, m);
            sw.Close();
        }

        static void call(int n, int m)
        {
            under(n);
            sw.WriteLine(arr[0]);
            if (n == m)
            {
                under(n);
                sw.WriteLine(arr[4]);
                under(n);
                sw.WriteLine(arr[5]);
                return;
            }
            under(n);
            sw.WriteLine(arr[1]);
            under(n);
            sw.WriteLine(arr[2]);
            under(n);
            sw.WriteLine(arr[3]);

            call(n + 1, m);
            under(n);
            sw.WriteLine(arr[5]);
        }

        static void under(int n)
        {
            for (int i = 0; i < n; i++)
                sw.Write(arr[6]);
        }
    }
}